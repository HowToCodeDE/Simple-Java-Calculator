package locic;

public class MathFunctions {
    public static double plusCalc (double num1, double num2) {
        return num1 + num2;
    }
    public static double minusCalc (double num1, double num2) {
        return num1 - num2;
    }
    public  static double multiplyCalc (double num1, double num2)
    {
        return num1 * num2;
    }
    public static double divideCalc (double num1, double num2) {
        return num1 / num2;
    }
}
